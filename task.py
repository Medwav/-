def получить_цены_стульев():
    """Получает цены на стулья у пользователя."""
    try:
        цена1 = int(input('Введите стоимость первого стула: '))
        цена2 = int(input('Введите стоимость второго стула: '))
        цена3 = int(input('Введите стоимость третьего стула: '))
        return цена1, цена2, цена3
    except ValueError:
        print("Ошибка: Пожалуйста, введите целые числа")
        return None

def рассчитать_итог(цена1, цена2, цена3):
    """Вычисляет итоговую сумму со скидкой."""
    общая_сумма = цена1 + цена2 + цена3

    if общая_сумма > 10000:
        скидка = общая_сумма * 0.10
        общая_сумма -= скидка

    return общая_сумма

def main():
    print("=== Расчёт стоимости стульев ===")

    # Получаем цены
    цены = получить_цены_стульев()
    if цены is None:
        return

    цена1, цена2, цена3 = цены

    # Вычисляем итог
    итоговая_сумма = рассчитать_итог(цена1, цена2, цена3)

    # Выводим результат
    print(f'\nИтоговая сумма чека составляет: {итоговая_сумма:.2f} рублей')

if __name__ == "__main__":
    main()